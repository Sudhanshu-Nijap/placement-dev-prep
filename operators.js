// logical operators

// logical and(&&) - if both are true then only satisfied or else not
console.log(true && true)
console.log(false && false)
console.log(false && true)

// logical or(||) - if any is true satisfied or else not
console.log(true || true)
console.log(false || false)
console.log(false || true)


// logical not(!) - makes it opposite
console.log(!false)
console.log(!true)